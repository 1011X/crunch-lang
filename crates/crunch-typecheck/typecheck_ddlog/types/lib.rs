    // Range
    pub fn std_range<A: Clone + Ord + ops::Add<Output = A> + PartialOrd>(
        from: &A,
        to: &A,
        step: &A,
    ) -> std_Vec<A> {
        let mut vec = std_Vec::new();
        let mut x = from.clone();
        while x <= *to {
            vec.push(x.clone());
            x = x + step.clone();
        }
        vec
    }

